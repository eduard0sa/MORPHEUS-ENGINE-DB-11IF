--VIEW PARA LISTAR O NÚMERO DE PESSOAS POR METRO QUADRADO DE CADA CIDADE.
CREATE VIEW VW_PEOPLE_PER_SQUARE_METER AS (SELECT CITY_NAME, 
(CITY_POPULATION/CITY_SURFACE_AREA) AS PEOPLE_PER_SQUARE_METER
FROM VW_CITIES_DEMOGRAPHIC_DATA (NOLOCK))
GO


--VIEW PARA LISTAR AS CIDADES MENOS SAUDÁVEIS.
CREATE VIEW VW_UNHEALTHY_CITIES AS (SELECT TOP (5) CITIES.CITY_NAME, CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_TEMPERATURE, CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_HUMIDITY FROM CITIES (NOLOCK)
INNER JOIN CITY_ENVIRONMENTAL_DATA (NOLOCK) ON CITIES.CITY_ID = CITY_ENVIRONMENTAL_DATA.CITY_ID
WHERE (CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_TEMPERATURE - 26) > 5
AND (CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_HUMIDITY - 50) > 15);
GO

--VIEW PARA LISTAR AS CIDADES MAIS SAUDÁVEIS.
CREATE VIEW VW_HEALTHY_CITIES AS (SELECT TOP (5) CITIES.CITY_NAME, CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_TEMPERATURE, CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_HUMIDITY FROM CITIES (NOLOCK)
INNER JOIN CITY_ENVIRONMENTAL_DATA (NOLOCK) ON CITIES.CITY_ID = CITY_ENVIRONMENTAL_DATA.CITY_ID
WHERE (CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_TEMPERATURE - 26) <= 5
AND (CITY_ENVIRONMENTAL_DATA.CITY_AVERAGE_HUMIDITY - 50) <= 15);
GO

--VIEW PARA LISTAR AS 5 CIDADES COM MENOR CUSTO DE VIDA.
CREATE VIEW VW_LIFE_COST AS (SELECT TOP (5) VW_CITIES.CITY_NAME, VW_CITIES_ECONOMICAL_DATA.CITY_LIFE_COST, VW_CITIES_ECONOMICAL_DATA.CITY_GDP, VW_CITIES_ECONOMICAL_DATA.CITY_AVERAGE_INCOME 
FROM VW_CITIES (NOLOCK) RIGHT JOIN VW_CITIES_ECONOMICAL_DATA 
(NOLOCK) ON VW_CITIES.CITY_ID = VW_CITIES_ECONOMICAL_DATA.CITY_ID 
WHERE (VW_CITIES_ECONOMICAL_DATA.CITY_AVERAGE_INCOME - VW_CITIES_ECONOMICAL_DATA.CITY_LIFE_COST) > 0);
GO

--VIEW PARA LISTAR O RENDIMENTO DE CADA SISTEMA DE SUPORTE À VIDA DE OXIGÉNIO RELATIVAMENTE À SUA CAPACIDADE MÁXIMA DE PRODUÇÃO.
CREATE VIEW PERFORMANCE_OF_OXIGEN_PRODUCTION AS (SELECT LOG_ID, (CAST(ROUND(CAST(LOG_OXIGEN_OUTPUT AS FLOAT) / CAST(LIFE_SUPPORT_SYSTEMS.SYSTEM_MAX_OUTPUT_CAPACITY AS FLOAT)*100, 2) AS VARCHAR) + ' %') AS OXIGEN_OUTPUT_RATE
FROM OXIGEN_PRODUCTION_LOGS (NOLOCK) INNER JOIN LIFE_SUPPORT_SYSTEMS (NOLOCK) ON OXIGEN_PRODUCTION_LOGS.LOG_SOURCE_ID = LIFE_SUPPORT_SYSTEMS.SYSTEM_ID);
GO

--VIEW PARA LISTAR O RENDIMENTO DE CADA SISTEMA DE SUPORTE À VIDA DE ÁGUA RELATIVAMENTE À SUA CAPACIDADE MÁXIMA DE PRODUÇÃO.
CREATE VIEW PERFORMANCE_OF_WATER_EXTRACTION AS (SELECT LOG_ID, (CAST(ROUND(CAST(LOG_WATER_OUTPUT AS FLOAT) / CAST(LIFE_SUPPORT_SYSTEMS.SYSTEM_MAX_OUTPUT_CAPACITY AS FLOAT)*100, 2) AS VARCHAR) + ' %') AS WATER_OUTPUT_RATE
FROM WATER_EXTRACTION_LOGS (NOLOCK) INNER JOIN LIFE_SUPPORT_SYSTEMS (NOLOCK) ON WATER_EXTRACTION_LOGS.LOG_SOURCE_ID = LIFE_SUPPORT_SYSTEMS.SYSTEM_ID);
GO