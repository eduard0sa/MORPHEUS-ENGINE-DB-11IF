--VIEW PARA LISTAR TODOS OS DADOS RELATIVOS �S CIDADES
CREATE VIEW VW_CITIES AS (SELECT [CITY_ID]
,CITIES.CITY_NAME
,CITIES.CITY_LOCATION
,CITIES.CITY_BUILT_DATE
,CITY_CONSTRUCTION_STATUS.CITY_CONSTRUCTION_STATUS_NAME
,CITIES.CITY_PRESIDENT_ID,
USER_FIRST_NAME AS PRESIDENT_FIRST_NAME,
USER_LAST_NAME AS PRESIDENT_LAST_NAME,
USER_EMAIL AS PRESIDENT_EMAIL,
USER_PHONENUMBER AS PRESIDENT_PHONENUMBER,
USER_PROFILE_PICTURE_PATH AS PRESIDENT_PROFILE_PICTURE_PATH,
USER_BIRTHDATE AS PRESIDENT_BIRTHDATE,
USER_GENDER_NAME AS PRESIDENT_GENDER,
COUNTRY_NAME AS PRESIDENT_COUNTRY,
USER_JOB_NAME AS PRESIDENT_JOB,
USER_ACADEMIC_LEVEL_NAME AS PRESIDENT_ACADEMIC_LEVEL
FROM CITIES
INNER JOIN VW_USERS_NON_SENSITIVE_INFORMATION ON CITIES.CITY_PRESIDENT_ID = VW_USERS_NON_SENSITIVE_INFORMATION.USER_ID
INNER JOIN CITY_CONSTRUCTION_STATUS ON CITY_CONSTRUCTION_STATUS.CITY_CONSTRUCTION_STATUS_ID = CITIES.CITY_CONSTRUCTION_STATUS_ID)

GO

