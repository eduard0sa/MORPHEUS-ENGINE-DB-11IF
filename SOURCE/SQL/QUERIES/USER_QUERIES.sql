--QUERY PARA CALCULAR A PERCENTAGEM DE UTILIZADORES COM NIVEL ACADÉMICO SUPERIOR OU IGUAL Á LICENCIATURA (ASSOCIATE DEGREE)
WITH SUBQUERY1 AS (SELECT COUNT(*) AS COUNT_USERS FROM VW_USER_CARACTERISTICS),
SUBQUERY2 AS (SELECT COUNT(*) AS COUNT_USERS_ABOVE_MASTERS FROM VW_USER_CARACTERISTICS WHERE USER_ACADEMIC_LEVEL_ID >= 4)
SELECT CAST(ROUND(dbo.FN_CALC_PERCENTAGE(SUBQUERY1.COUNT_USERS, SUBQUERY2.COUNT_USERS_ABOVE_MASTERS), 2) AS VARCHAR) + ' %' AS USERS_ABOVE_MASTERS_PERCENTAGE FROM SUBQUERY1 FULL JOIN SUBQUERY2 ON 1 = 1;
GO

