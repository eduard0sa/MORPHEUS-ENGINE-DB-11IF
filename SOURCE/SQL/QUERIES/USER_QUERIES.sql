--QUERY PARA CALCULAR A PERCENTAGEM DE UTILIZADORES QUE SÃO CEO's DE ALGUMA EMPRESA
WITH SUBQUERY AS (SELECT DISTINCT USER_FIRST_NAME AS USER_FIRST_NAME FROM VW_COMPANY_DATA),
SUBQUERY1 AS (SELECT COUNT(SUBQUERY.USER_FIRST_NAME) AS COUNT_CEO_USERS FROM SUBQUERY),
SUBQUERY2 AS (SELECT COUNT(*) AS COUNT_TOTAL_USERS FROM VW_USER_CARACTERISTICS)
SELECT CAST(ROUND(dbo.FN_CALC_PERCENTAGE(SUBQUERY2.COUNT_TOTAL_USERS, SUBQUERY1.COUNT_CEO_USERS), 2) AS VARCHAR) + ' %' FROM SUBQUERY1 FULL JOIN SUBQUERY2 ON 1=1

