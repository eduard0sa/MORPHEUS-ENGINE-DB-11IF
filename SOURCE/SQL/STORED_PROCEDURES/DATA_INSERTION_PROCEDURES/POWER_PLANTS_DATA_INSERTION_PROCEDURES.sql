--PROCEDURE PARA INSERIR UM REGISTO DE MANUTENï¿½ï¿½ï¿½ï¿½O DE UMA CENTRAL ELï¿½TRICA
CREATE PROCEDURE SP_INSERT_POWER_PLANT_MAINTENANCE_LOG
@POWER_PLANT_ID INT,
@MAINTENANCE_DESCRIPTION VARCHAR(MAX),
@MAINTENANCE_DATE DATE,
@MAINTENANCE_TECHNITIAN_ID INT,
@MAINTENANCE_RESULTS_INFO VARBINARY(MAX),
@NEXT_MAINTENANCE_DATE DATE
AS
BEGIN
	IF((SELECT COUNT(*) FROM POWER_PLANTS WHERE POWER_PLANT_ID = @POWER_PLANT_ID) > 0)
	BEGIN
		INSERT INTO POWER_PLANT_MAINTENANCE_LOGS (
			POWER_PLANT_ID,
			MAINTENANCE_DESCRIPTION,
			MAINTENANCE_DATE,
			MAINTENANCE_TECHNITIAN_ID,
			MAINTENANCE_RESULTS_INFO
		) VALUES (
			@POWER_PLANT_ID,
			@MAINTENANCE_DESCRIPTION,
			@MAINTENANCE_DATE,
			@MAINTENANCE_TECHNITIAN_ID,
			@MAINTENANCE_RESULTS_INFO
		);

		DECLARE @COUNT_POWER_PLANT_MAINTENANCE_LOGS INT;
		SET @COUNT_POWER_PLANT_MAINTENANCE_LOGS = (SELECT COUNT(*) FROM POWER_PLANT_MAINTENANCE_LOGS WHERE POWER_PLANT_ID = @POWER_PLANT_ID);

		INSERT INTO OPERATION_LOGS (
			OPERATION_NAME,
			OPERATION_CATEGORY_ID,
			OPERATION_STATUS_ID
		) VALUES (
			'SP_POWERP_MAINT-' + (SELECT POWER_PLANT_NAME FROM POWER_PLANTS WHERE POWER_PLANT_ID = @POWER_PLANT_ID) + '_' + @COUNT_POWER_PLANT_MAINTENANCE_LOGS,
			2, -- CATEGORIA DE MANUTENï¿½ï¿½ï¿½ï¿½O
			1  -- ESTADO 'COMPLETO (COMPLETED)'
		);

		UPDATE POWER_PLANTS
			SET POWER_PLANT_NEXT_MAINTENANCE_DATE = @NEXT_MAINTENANCE_DATE
			WHERE POWER_PLANT_ID = @POWER_PLANT_ID;
	END
	ELSE
	BEGIN
		PRINT 'A central elï¿½trica nï¿½ï¿½o existe.';
	END
END

