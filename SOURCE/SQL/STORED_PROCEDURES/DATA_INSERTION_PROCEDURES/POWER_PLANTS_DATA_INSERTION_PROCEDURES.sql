@POWER_PLANT_ID INT,
@POWER_STORAGE_DEVICE_MANUFACTURER_ID INT,
@POWER_STORAGE_DEVICE_NAME VARCHAR(50),
@POWER_STORAGE_DEVICE_MAX_CAPACITY FLOAT,
@POWER_STORAGE_DEVICE_STATUS_ID INT
AS
BEGIN
	IF((SELECT COUNT(*)
		FROM POWER_PLANTS
		WHERE POWER_PLANT_ID = @POWER_PLANT_ID) > 0)
	BEGIN
		IF((SELECT COUNT(*)
			FROM POWER_STORAGE_DEVICES
			WHERE POWER_STORAGE_DEVICE_NAME = @POWER_STORAGE_DEVICE_NAME) > 0)
		BEGIN
			PRINT 'O dispositivo de armazenamento de energia jÃ¡ existe.';
		END
		ELSE
		BEGIN
			IF((SELECT COUNT(*) FROM COMPANIES WHERE COMPANY_ID = @POWER_STORAGE_DEVICE_MANUFACTURER_ID) = 0)
			BEGIN
				PRINT 'O fabricante do dispositivo de armazenamento de energia nï¿½ï¿½o existe.';
				
			END
			ELSE
			BEGIN
				INSERT INTO POWER_STORAGE_DEVICES (
					POWER_PLANT_ID,
					POWER_STORAGE_DEVICE_MANUFACTURER_ID,
					POWER_STORAGE_DEVICE_NAME,
					POWER_STORAGE_DEVICE_MAX_CAPACITY,
					POWER_STORAGE_DEVICE_STATUS_ID
				) VALUES (
					@POWER_PLANT_ID,
					@POWER_STORAGE_DEVICE_MANUFACTURER_ID,
					@POWER_STORAGE_DEVICE_NAME,
					@POWER_STORAGE_DEVICE_MAX_CAPACITY,
					@POWER_STORAGE_DEVICE_STATUS_ID
				);
			END
		END	
	END
	ELSE
	BEGIN
		PRINT 'A central elï¿½trica nï¿½ï¿½o existe.';
	END
END

