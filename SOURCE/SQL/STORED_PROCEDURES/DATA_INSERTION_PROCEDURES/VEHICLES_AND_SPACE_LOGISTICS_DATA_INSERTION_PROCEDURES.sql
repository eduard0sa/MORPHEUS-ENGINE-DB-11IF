--#region SPACE_LOGISTICS

--STORED PROCEDURE PARA A INSERS’┐ĮO DE MISS’┐ĮES ESPACIAIS LOGISTICAS NA TABELA 'SPACE_LOGISTICS'
CREATE PROCEDURE SP_INSERT_SPACE_LOGISTICS
@MISSION_NAME varchar(100),
@MISSION_TYPE_ID int,
@MISSION_COMPANY_ID int,
@MISSION_TRANSPORT_VEHICLE_ID int,
@DESCRIPTION text,
@LAUNCH_DATE date,
@LANDING_DATE date,
@LAUNCH_LOCATION varchar(50),
@LANDING_LOCATION varchar(50),
@BUDGET float
AS
BEGIN
	INSERT INTO OPERATION_LOGS(OPERATION_NAME, OPERATION_CATEGORY_ID, OPERATION_STATUS_ID) VALUES('SP_LG-' + @MISSION_NAME, 1, 1) --INSERS’┐ĮO DA OPERA’┐Į’┐ĮO A QUE ESTA MISS’┐ĮO EST’┐Į ASSOCIADA (REFERENCIA NA TABELA OPERATION_LOGS)
	DECLARE @LAST_OPERATION_ID INT;
	SET	@LAST_OPERATION_ID = (SELECT MAX(OPERATION_LOGS.OPERATION_ID) FROM OPERATION_LOGS);

	--INSERS’┐ĮO DA MISS’┐ĮO NA TABELA SPACE_LOGISTICS
	INSERT INTO SPACE_LOGISTICS(OPERATION_ID, MISSION_NAME, MISSION_TYPE_ID, MISSION_COMPANY_ID, MISSION_TRANSPORT_VEHICLE_ID, DESCRIPTION, LAUNCH_DATE, LANDING_DATE, LAUNCH_LOCATION, LANDING_LOCATION, BUDGET) VALUES(@LAST_OPERATION_ID,
	@MISSION_NAME,
	@MISSION_TYPE_ID,
	@MISSION_COMPANY_ID,
	@MISSION_TRANSPORT_VEHICLE_ID,
	@DESCRIPTION,
	@LAUNCH_DATE,
	@LANDING_DATE,
	@LAUNCH_LOCATION,
	@LANDING_LOCATION,
	@BUDGET)
END

--#endregion SPACE_LOGISTICS

GO

--#region VEHICLES

--STORED PROCEDURE PARA INSERS’┐ĮO DE VE’┐ĮCULOS, NA TABELA 'VEHICLES'
CREATE PROCEDURE SP_INSERT_VEHICLES
@VEHICLE_SERIAL_NUMBER varchar(50),
@VEHICLE_MANUFACTURER_ID int,
@VEHICLE_PRODUCTION_DATE date,
@VEHICLE_NAME varchar(100),
@VEHICLE_TYPE_ID int,
@VEHICLE_MASS float,
@VEHICLE_LENGTH float,
@VEHICLE_WIDTH float,
@VEHICLE_HEIGHT float,
@VEHICLE_FUEL_TYPE_ID int,
@VEHICLE_NEXT_MAINTENANCE_DATE date
AS
BEGIN
	--INSERS’┐ĮO DO VE’┐ĮCULO, NA TABELA 'VEHICLES'
	INSERT INTO VEHICLES(VEHICLE_SERIAL_NUMBER, VEHICLE_MANUFACTURER_ID, VEHICLE_PRODUCTION_DATE, VEHICLE_NAME, VEHICLE_TYPE_ID, VEHICLE_MASS, VEHICLE_LENGTH, VEHICLE_WIDTH, VEHICLE_HEIGHT, VEHICLE_FUEL_TYPE_ID, VEHICLE_NEXT_MAINTENANCE_DATE) VALUES (
	@VEHICLE_SERIAL_NUMBER,
	@VEHICLE_MANUFACTURER_ID,
	@VEHICLE_PRODUCTION_DATE,
	@VEHICLE_NAME,
	@VEHICLE_TYPE_ID,
	@VEHICLE_MASS,
	@VEHICLE_LENGTH,
	@VEHICLE_WIDTH,
	@VEHICLE_HEIGHT,
	@VEHICLE_FUEL_TYPE_ID,
	@VEHICLE_NEXT_MAINTENANCE_DATE);
END

--#endregion VEHICLES