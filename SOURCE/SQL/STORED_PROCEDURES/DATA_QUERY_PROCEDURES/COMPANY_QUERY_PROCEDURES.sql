--PROCEDURE PARA LISTAR OS UTILIZADORES COM UMA DETERMINADA PROFISSÃO, DE UMA DETERMINADA EMPRESA (OPCIONAL).
CREATE PROCEDURE SP_LIST_COMPANY_USERS_BY_JOB
@JOB_NAME VARCHAR(75), @COMPANY_NAME VARCHAR(100)
AS
BEGIN
    IF(@COMPANY_NAME != '')
    BEGIN
        SELECT USER_JOB_NAME, EMPLOYER_COMPANY_NAME,
        USER_ID,
        USER_FIRST_NAME + ' ' + USER_LAST_NAME AS USER_FULL_NAME,
        USER_EMAIL,
        USER_PHONENUMBER,
        USER_PROFILE_PICTURE_PATH,
        USER_BIRTHDATE,
        USER_GENDER_NAME,
        COUNTRY_NAME,
        USER_ADRESS,
        USER_ACADEMIC_LEVEL_NAME,
        USER_LEVEL_NAME
        FROM VW_USERS_NON_SENSITIVE_INFORMATION (NOLOCK)
        WHERE USER_JOB_NAME = @JOB_NAME AND @COMPANY_NAME = EMPLOYER_COMPANY_NAME
    END
    ELSE
    BEGIN
        SELECT USER_JOB_NAME, EMPLOYER_COMPANY_NAME,
        USER_ID,
        USER_FIRST_NAME + ' ' + USER_LAST_NAME AS USER_FULL_NAME,
        USER_EMAIL,
        USER_PHONENUMBER,
        USER_PROFILE_PICTURE_PATH,
        USER_BIRTHDATE,
        USER_GENDER_NAME,
        COUNTRY_NAME,
        USER_ADRESS,
        USER_ACADEMIC_LEVEL_NAME,
        USER_LEVEL_NAME
        FROM VW_USERS_NON_SENSITIVE_INFORMATION (NOLOCK)
        WHERE USER_JOB_NAME = @JOB_NAME
    END
END

GO

CREATE PROCEDURE SP_COUNT_USERS_BY_COMPANY
@COMPANY_NAME VARCHAR(100)
AS
BEGIN
    SELECT COUNT(*) AS TOTAL_USERS
    FROM VW_USERS_BASIC_INFORMATION (NOLOCK)
    WHERE @COMPANY_NAME = EMPLOYER_COMPANY_NAME
END